export default function visualizeClassName(name) {
    let className = "";

    if (name === "class1") {
        className = "1 Клас";
    } else if (name === "class2") {
        className = "2 Клас";
    } else if (name === "class3") {
        className = "3 Клас";
    } else if (name === "class4") {
        className = "4 Клас";
    }

    return className;
}
